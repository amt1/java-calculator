public class Calculator {

    public Calculator() {

    }

    public int add(int int1, int int2){
        return int1 + int2;
    }

    public int subtract(int int1, int int2){
        return int1 - int2;
    }

    public int multiply(int int1, int int2){
        return int1 * int2;
    }

    public float divide(float num1, float num2){
        return  (num1 / num2);
    }
}
